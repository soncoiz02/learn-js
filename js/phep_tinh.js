// Tính chu vi, diện tích của hình chữ nhật có chiều dài là 3, rộng là 4
let chieuDai = 3;
let chieuRong = 4;
let chuVi = (chieuDai + chieuRong) * 2;
console.log(chuVi);
let dienTich = chieuDai * chieuRong;
console.log(dienTich);

// Chia lấy số dư: %
let soDu = 15 % 2; // Dư 1
console.log(soDu);

